Toute personne qui postule pour un emploi qui la mettrait en présence d'enfants ou de personnes vulnérables pourra faire l'objet d'une vérification de son casier judiciaire. 
Cette vérification permettra de déterminer si le postulant a obtenu une réhabilitation pour une infraction de nature sexuelle.  
Outre ce mécanisme, le projet de loi C-7 apporte d'autres modifications à la Loi sur le casier judiciaire. 
