LA JOURNÉE BRAILLE 
M. Mac Harb (Ottawa-Centre, Lib.):
Monsieur le Président, c'est aujourd'hui la première Journée Braille, promulguée par notre vice-premier ministre.  
Nous rendons hommage aujourd'hui à la mémoire de Louis Braille. 
C'est à lui que nous devons la création d'un système de six points saillants qui a révolutionné dans le monde entier la vie des aveugles et des personnes ayant une déficience visuelle.  
Grâce à une combinaison de six points magiques, une personne aveugle est capable de lire. 
La capacité de lire un message imprimé ouvre d'innombrables portes.  
La Journée Braille est une occasion de souligner l'importance de cette invention et son impact sur l'alphabétisme.  
J'invite mes collègues à se joindre à moi en ce jour de célébration. 
Le thème de cette année est Braille égale Égalité.  
