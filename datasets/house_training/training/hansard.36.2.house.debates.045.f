M. Jacques Saada (secrétaire parlementaire du solliciteur général du Canada, Lib.):
Monsieur le Président, il me fait plaisir d'intervenir aujourd'hui à la Chambre sur le projet de loi C-7, Loi modifiant la Loi sur le casier judiciaire.  
On se souviendra que ce même projet de loi avait recueilli l'appui unanime de tous les partis au cours de la session parlementaire précédente quand il portait le numéro C-69. 
Je suis heureux de signaler que le projet de loi C-7 a reçu l'aval unanime des sénateurs de l'autre Chambre à l'étape de la troisième lecture.  
De plus, et je crois que c'est important de le souligner, tous les ministres de la Justice et solliciteurs généraux des administrations fédérale, provinciales et territoriales ont accordé leur appui au document dans ses grandes lignes. 
À terme, le projet de loi C-7 nous aidera à mieux assurer la sécurité de nos enfants et d'autres personnes vulnérables.  
Le projet de loi C-7 nous aidera à mieux protéger nos enfants en permettant de mettre une indication spéciale dans le système du Centre d'information de la police canadienne, qu'on appelle aussi CIPC, afin de signaler aux corps policiers effectuant une vérification, qu'une personne réhabilitée a déjà été reconnue coupable d'une infraction sexuelle. 
On pourra ensuite demander à la direction générale du CIPC de lever les scellés sur le dossier judiciaire en question.  
Ainsi, les organismes chargés de prendre soin d'enfants, qui envisagent de recruter un employé ou un bénévole, auront à leur disposition un système plus performant qui permettra de mieux repérer un postulant ayant obtenu une réhabilitation relativement à une condamnation pour une infraction sexuelle. 
L'indication spéciale permettra d'éviter que de tels dossiers échappent aux vérifications de casiers judiciaires effectuées pour les besoins du filtrage.  
Mes distingués collègues se souviendront sans doute que le Comité sénatorial permanent des affaires juridiques et constitutionnelles avait émis certaines réserves au sujet du projet de loi. 
Les honorables sénateurs ont donc adopté des motions visant à l'amender.  
Sans porter atteinte à l'essence du texte adopté par les députés de cette Chambre, les motions se traduisent par quatre amendements qui modifient la structure du projet de loi pour en améliorer la mise en application.  
Le premier amendement précise que le système de repérage s'applique uniquement aux infractions sexuelles. 
D'ailleurs, il a toujours été très clair que ce projet de loi visait ce type d'infractions, et pas d'autres.  
Le deuxième amendement retranche la liste des infractions sexuelles visées du règlement pour l'insérer en annexe à la loi. 
Cela dit, la liste des infractions n'a pas été modifiée comme telle depuis qu'elle avait été étudiée par le Comité permanent de la justice et des droits de la personne.  
Le troisième amendement retranche les définitions des termes «enfant» et «personne vulnérable» du règlement pour les incorporer dans la loi.  
Enfin, le quatrième amendement modifie la forme et non pas le fond de la définition du terme «personne vulnérable», en enlevant le mot «handicap», mais en conservant le mot «infirmité».  
J'aimerais remercier les membres du Comité sénatorial permanent des affaires juridiques et constitutionnelles de leurs observations judicieuses. 
Je suis convaincu que mes distingués collègues seront satisfaits des amendements proposés qui, je le répète, portent sur la forme et non sur le fond du projet de loi adopté par les députés de cette Chambre.  
