LA JOURNÉE D'AFFRANCHISSEMENT DE LA NOTE D'ÉPICERIE 
Mme Rose-Marie Ur (Lambton-Kent-Middlesex, Lib.):
Nous bénéficions du système d'approvisionnement en nourriture le plus économique au monde. 
Nous ne consacrons que 9,8 p. 100 de notre revenu disponible à la nourriture contre 11 p. 100 aux États-Unis et 33 p. 100 au Mexique. 
Nos agriculteurs produisent des denrées alimentaires saines et de haute qualité à des prix concurrentiels, mais leur part des profits diminue.  
Combien de Canadiens savent que si un pain coûte 1,50 $, l'agriculteur ne touche que 9 cents ou que, si nous payons un verre de lait 1,50 $, moins de 16 cents vont au producteur? 
Une serveuse ou un serveur gagnent proportionnellement plus en pourboire sur un repas que l'agriculteur qui a produit les denrées qui ont servi à le préparer.  
Nos agriculteurs sont les plus efficaces et les plus concurrentiels au monde, mais les bénéfices qu'ils font sur les denrées brutes qu'ils produisent vont en diminuant bien que les exportations ne cessent d'augmenter.  
Célébrons la Journée d'affranchissement de la note d'épicerie tout en nous rappelant que les agriculteurs doivent obtenir un prix équitable pour leurs denrées, sinon ils n'auront pas les moyens de continuer à produire.  
