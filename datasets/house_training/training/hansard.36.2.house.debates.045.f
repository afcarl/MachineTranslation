LE SYSTÈME DE JUSTICE PÉNALE POUR LES ADOLESCENTS 
M. Chuck Cadman (Surrey-Nord, Réf.): 
Monsieur le Président, les Canadiens en ont assez d'entendre le gouvernement promettre de présenter un texte de loi qui soit efficace en matière de système de justice pénale pour les adolescents.  
Bientôt, cela fera trois ans que la ministre de la Justice détient cette charge. 
Après l'avoir promis des mois durant, elle a enfin présenté les projets de loi C-68 et C-3 pour remplacer la Loi sur les jeunes contrevenants, qui avait suscité de fortes controverses.  
Il a longuement été question dans les communiqués publiés à ce sujet de sévir plus durement contre les contrevenants. 
Or les Canadiens découvrent aujourd'hui que ces initiatives favorisent encore trop d'indulgence à l'égard des jeunes contrevenants violents.  
Les Canadiens commencent à ruer dans les brancards. 
Deux individus, Bruce McGloan de Calgary et Joseph Wamback de Newmarket en Ontario, ont conjugué leurs efforts pour faire signer des pétitions contestant les initiatives ministérielles en matière de justice pénale pour les adolescents. 
À ce jour, ils comptent des centaines de milliers de partisans.  
Ces hommes ne sont que deux parmi un grand nombre de personnes qui ont souffert des conséquences de crimes violents perpétrés contre leurs enfants. 
Bruce a perdu son fils, mort aux mains d'un jeune assassin. 
Le fils de Joseph souffre, pour sa part, de paralysie par suite d'une violente agression. 
Il convient de féliciter ces deux pères pour leurs efforts, et de rappeler au gouvernement ses piètres initiatives dans le domaine.  
