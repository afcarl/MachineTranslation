Pour bien comprendre la teneur des modifications proposées, permettez-moi de faire un rappel du contexte dans lequel la loi actuelle a été adoptée et de ses dispositions.  
Le Rapport Ouimet recommandait en 1969 l'adoption d'une loi canadienne sur le pardon. 
À cette époque, on souhaitait instaurer des mesures pour favoriser l'inclusion dans la société des contrevenants qui avaient observé une période de bonne conduite depuis la commission de leur crime.  
La Loi sur le casier judiciaire a été adoptée en 1985. 
Cette loi permet au contrevenant de présenter une demande de réhabilitation. 
Elle contient les conditions et la procédure à suivre pour obtenir ce qu'on appelle communément le pardon.  
Selon les dispositions de la loi actuelle, une personne est admissible à la réhabilitation si elle observe une période de bonne conduite suite à l'expiration légale de sa peine. 
Cette période est de cinq ans, si la personne a été déclarée coupable d'une infraction punissable par voie de mise en accusation, et de trois ans, si elle a été déclarée coupable d'une infraction punissable sur déclaration sommaire de culpabilité.  
Quels sont les effets d'une réhabilitation pour le contrevenant? 
Selon l'article 5 de la loi, l'octroi d'une réhabilitation fait preuve de la bonne conduite du demandeur. 
Elle rétablit sa réputation et efface les conséquences de la condamnation. 
D'autres, s'appuyant sur le texte de la version anglaise qui contient l'expression «vacates the conviction», soutiennent que la réhabilitation met simplement à l'écart les informations relatives à la condamnation.  
Cette question a d'ailleurs été débattue par la Cour d'appel du Québec dans l'affaire de l'honorable juge Richard Therrien. 
La Cour est arrivée à la conclusion que: «l'interprétation la plus sûre de la loi reconnaît que le pardon n'emporte pas une annulation rétroactive de la condamnation, mais qu'elle mitige les effets de cette dernière en éteignant ceux-ci, autant que possible, et en interdisant la discrimination à l'égard du bénéficiaire de cette mesure».  
La Cour ajoute que: «l'article 5 de la Loi sur le casier judiciaire s'avère ainsi d'une portée limitée. 
En pratique, l'obtention de la réhabilitation élimine les antécédents criminels du fichier informatisé des relevés de condamnations criminelles qu'on appelle le Centre d'information de la police canadienne. 
C'est la banque officielle des casiers judiciaires au Canada. 
Lorsqu'on accorde une réhabilitation, le casier judiciaire est retiré du système et il n'est plus accessible, sauf sur autorisation du solliciteur général du Canada.  
La loi actuelle prévoit qu'une réhabilitation peut être révoquée. 
Le pardon d'une personne peut être révoqué si elle est condamnée pour une nouvelle infraction criminelle punissable sur déclaration sommaire de culpabilité, ou si elle a cessé de bien se conduire, ou si elle a fait une déclaration inexacte ou trompeuse, ou si elle a dissimulé un fait important. 
Par contre, la personne qui est condamnée à une infraction criminelle punissable par voie de mise en accusation voit son pardon automatiquement annulé.  
La Loi sur le casier judiciaire prévoit que les demandes de réhabilitation sont présentées à la Commission nationale des libérations conditionnelles. 
La Commission fait une enquête sur le demandeur. 
Si la Commission nationale des libérations conditionnelles décide de ne pas accorder le pardon, aucune disposition dans la loi actuelle n'empêche le demandeur de faire une nouvelle demande immédiatement après le refus.  
