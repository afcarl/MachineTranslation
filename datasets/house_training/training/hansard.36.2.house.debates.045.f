LA COMMISSION DE L'IMMIGRATION ET DU STATUT DE RÉFUGIÉ 
M. David Price (Compton-Stanstead, PC):
Monsieur le Président, le 18 octobre dernier, j'ai interrogé la ministre de la Citoyenneté et de l'Immigration sur le troublant processus de sélection des candidats à la présidence de la Commission de l'immigration et du statut de réfugié.  
Trop souvent, des libéraux ayant des relations sont nommés à des postes d'autorité et de prestige. 
J'implore la ministre de faire participer des parlementaires, et notamment des membres de comité, à ce processus de sélection. 
Les parlementaires ont le mandat de représenter leurs électeurs et de travailler pour eux. 
Je songe en particulier à la nomination par décret, le 16 novembre 1999, de M. Peter Showler à la présidence de la Commission de l'immigration et du statut de réfugié. 
Je me réjouis de cette nomination; M. Showler a toute la compétence voulue pour relever le défi de taille que constitue la présidence de la CISR, en soi un poste quasi-judiciaire.  
Mon objection en est une de principe. 
Pourquoi n'a-t-on pas consulté le Comité permanent de la citoyenneté et de l'immigration? 
...à faire une étude et présenter un rapport sur toutes les questions relatives au mandat, à l'administration et au fonctionnement des ministères qui leur sont confiés...  
L'alinéa 108(2) e) du Règlement étend aussi les pouvoirs du comité et lui permet d'étudier: 
d'autres questions liées au mandat, à l'administration, à l'organisation ou au fonctionnement du ministère que le comité juge bon d'examiner.  
La nomination du président de la CISR n'est-elle pas considérée comme un aspect capital de l'administration du ministère de la Citoyenneté et de l'Immigration?  
L'article 111 du Règlement prévoit que le comité peut examiner les nominations. 
L'examen des nominations, c'est bien beau, mais cela ne donne aux membres du comité aucun pouvoir à l'égard du processus d'embauche préalable.  
Les comités permanents connaissent à fond les dossiers et ils pourraient être d'excellent conseil quand vient le moment de nommer quelqu'un à la présidence de la CISR. 
Même si les conclusions de l'examen étaient négatives, celui-ci n'entraînerait pas nécessairement le rejet d'un candidat.  
J'ai peur que les comités et la Chambre ne soient en train de devenir des organes qui avalisent automatiquement les politiques et motions déjà formulées et approuvées par le Bureau du Conseil privé et le Cabinet du premier ministre.  
Le gouvernement consulte de moins en moins la Chambre, mais je tiens à rappeler au parti ministériel qu'il a obtenu l'appui de 38,5 p. 100 de l'électorat canadien lors des élections de 1997. 
C'est loin d'être une majorité. 
Parce que la Chambre des communes n'est pas consultée, la grande majorité des Canadiens n'a pas voix au chapitre. 
C'est une façon de faire inacceptable dans une démocratie libérale comme le Canada.  
Les membres des comités sont impuissants par rapport à ce que décident de faire le premier ministre et le Cabinet. 
Il est intéressant de souligner que l'Association du Barreau canadien et le Conseil canadien pour les réfugiés ont tous deux dénoncé le processus de sélection actuel. 
Ils veulent un mécanisme d'embauche plus juste et plus transparent.  
La prochaine fois qu'il y aura une nomination par décret, la ministre agira-t-elle de façon démocratique et vertueuse et consultera-t-elle le Comité permanent de la citoyenneté et de l'immigration?  
M. Andrew Telegdi (secrétaire parlementaire de la ministre de la Citoyenneté et de l'Immigration, Lib.):
Monsieur le Président, je rappelle que, en vertu de la Loi sur l'immigration, les membres de la Commission de l'immigration et du statut de réfugié, y compris le président, sont nommés par décret du gouverneur en conseil.  
Fidèle à la promesse de transparence du gouvernement, un avis de vacance du poste de président a été publié dans la Gazette du Canada le 26 juin 1999. Un tel avis donne à tous les candidats qualifiés la possibilité de poser leur candidature ou de présenter leur curriculum vitae au bureau du premier ministre.  
Voyons un peu ses compétences. 
M. Showler possède une vaste expérience comme juriste en droit de l'immigration et du statut de réfugié. 
Il a été à l'origine de nombreux programmes d'éducation publics et d'initiatives visant la réforme du droit. 
M. Showler a enseigné le droit de l'immigration et du statut de réfugié à l'Université d'Ottawa. 
Avant sa nomination, M. Showler a été pendant cinq ans membre de la Section du statut de réfugié au sens de la Convention.  
Tout ce que je peux dire, c'est qu'il s'agit là d'une excellente nomination et nous nous attendons à ce que M. Showler serve très bien le Canada.  
