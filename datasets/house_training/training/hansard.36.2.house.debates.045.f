DÉVELOPPEMENT DES RESSOURCES HUMAINES CANADA 
Mme Eleni Bakopanos (Ahuntsic, Lib.):
Monsieur le Président, j'aimerais souligner aujourd'hui l'importance et la nécessité des programmes de Développement des ressources humaines Canada, entre autres, le Fonds canadien pour la création d'emplois.  
Il est nécessaire de clarifier que le mandat du Fonds en question est de créer des emplois et de donner un soutien financier au secteur privé industriel afin de pouvoir concurrencer avec les entreprises à l'échelle mondiale.  
Depuis son arrivée au pouvoir, le gouvernement a créé plus de 1,7 million d'emplois. 
Dans ma circonscription, les fabricants de textiles, dont Christina Canada, Tricot Exclusive et Heritage Decoys, ont créé 239 emplois. 
En 1998-1999, il a été créé dans ma circonscription 291 emplois en tout.  
Ce n'est là qu'un seul programme dans une seule circonscription, mais je suis en mesure de vous parler de tous les autres programmes du ministère du Développement des ressources humaines, qui aident les Canadiens à trouver du travail, les jeunes décrocheurs à acquérir les compétences utiles sur le marché du travail, et les handicapés et les analphabètes à devenir des membres productifs de la société. 
Comment expliquer alors que l'opposition s'en prenne à ces programmes dont les bienfaits sont aussi nombreux pour tant de Canadiens? 
