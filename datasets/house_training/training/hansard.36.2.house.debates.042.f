M. Stan Dromisky (secrétaire parlementaire du ministre des Transports, Lib.):
Madame la Présidente, je suis très heureux d'avoir l'occasion de parler de l'idée d'élaborer une politique nationale du réseau routier.  
On peut voir dans la motion du député de Cumberland-Colchester une autre indication de l'importance que les Canadiens attachent à notre réseau routier national. 
Nous nous préoccupons tous en effet de l'infrastructure des transports de surface. 
Il est indispensable d'assurer la mobilité des personnes et des marchandises d'un bout à l'autre du Canada pour maintenir la qualité de vie des Canadiens et la compétitivité du Canada dans l'économie mondiale.  
Le réseau routier a toujours joué un rôle important dans le développement de l'économie canadienne. 
Le dernier discours du Trône reconnaissait d'ailleurs que notre réseau d'autoroutes, ainsi que d'autres réalisations comme la construction d'un chemin de fer national et la création d'un service postal et d'institutions culturelles nationales constituaient les assises de notre qualité de vie.  
Cela est effectivement vrai du système canadien de transport de surface. 
La construction de la route transcanadienne et du réseau ferroviaire en sont des exemples manifestes.  
Les autoroutes constituent en effet la colonne vertébrale des systèmes de transport du Canada. 
Les autoroutes, les routes et les rues jouent un rôle essentiel dans les échanges internes et le commerce international du Canada, depuis l'expédition initiale de matières premières jusqu'à la livraison des produits finis sur le marché.  
Les études révèlent que, au Canada, le réseau routier assure 90 p. 100 de tout le trafic passagers interurbain et 75 p. 100 du trafic marchandises, exprimés en dollars. 
Environ 4,5 milliards de voitures-kilomètres de ce trafic a lieu sur le réseau routier national.  
L'importance du transport pour notre secteur industriel est indéniable. 
En particulier, le transport représente une grosse proportion des coûts d'exportation des produits canadiens traditionnels, soit 45 p. 100 pour le charbon et 30 p. 100 pour le bois d'oeuvre.  
Nous savons tous que l'infrastructure routière coûte très cher à construire et à entretenir. 
Nous sommes également conscients que le réseau existant est en train de se détériorer très rapidement et que, plus nous attendons, plus les coûts de réparation vont augmenter.  
Le défi qui se pose donc aux gouvernements est de trouver un juste équilibre entre un réseau de transport de surface sûr et efficace et les autres priorités concurrentes de tout gouvernement. 
Il faut que nous reconnaissions que les investissements stratégiques de ce genre produisent des avantages énormes.  
En 1987, le conseil des ministres responsables des transports et de la sécurité routière ont fait faire une étude pluriannuelle sur la politique routière nationale. 
Cette étude a abouti à la création d'un réseau routier national qui englobe près de 24 400 kilomètres des routes canadiennes existantes.  
Par ailleurs, l'étude a estimé que, en 1992, le coût de la modernisation du réseau s'élèverait à près de 14 milliards de dollars.  
En juin 1997, ce même conseil des ministres responsables des transports et de la sécurité routière créait un groupe de travail fédéral-provincial-territorial et lui confiait le mandat de mettre à jour l'étude sur la politique routière nationale. 
Les auteurs de l'étude concluent qu'en dépit d'une augmentation de moitié des dépenses annuelles au cours de la dernière décennie et de la correction de certaines imperfections du réseau routier national, l'état de ce dernier n'a guère été amélioré. 
Si l'on se fonde sur les mêmes critères utilisés en 1988, la longueur du réseau jugée en mauvais état a augmenté de 30 p. 100. 
Le coût de la correction des imperfections identifiées du réseau routier national a été évalué à 17,4 milliards en 1998, une hausse de plus de 3 milliards de dollars en moins de dix ans.  
Appuyant un financement accru à la réfection du réseau routier national, l'étude donne une indication des avantages que procurerait un réseau routier amélioré. 
Sur une période de 25 ans, la valeur actuelle des avantages projetés du programme d'investissement dans le réseau routier devrait, selon les estimations, dépasser 30 milliards de dollars, soit 22 milliards de dollars d'économies en temps de déplacement, 5,8 milliards de dollars en améliorations de la sécurité routière, 2,9 milliards de dollars en coûts réduits d'entretien des véhicules et 1,3 milliard de dollars en avantages pour le réseau.  
Une diminution de la congestion et une amélioration des conditions routières pourraient avoir pour effet de réduire de 247 p. 100 le nombre d'accidents de la route mortels, et de 16 000 par année, le nombre de blessures causées par des accidents.  
L'amélioration du réseau routier devrait également avoir pour effet de diminuer de 236 millions de litres par année la consommation de carburant.  
L'étude précise aussi qu'un examen de la documentation et de l'expérience internationale donne des preuves convaincantes voulant que l'investissement dans le réseau routier peut se traduire par une hausse marquée de la productivité et le soutien du développement économique.  
