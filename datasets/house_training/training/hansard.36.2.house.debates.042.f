LA POLITIQUE NATIONALE DU RÉSEAU ROUTIER 
M. Bill Casey (Cumberland-Colchester, PC) propose: 
Que, de l'avis de la Chambre, le gouvernement devrait élaborer de concert avec les provinces une politique nationale du réseau routier afin d'assurer la viabilité à long terme de notre réseau routier national, compte tenu de la nature de notre pays, de notre géographie et de notre culture, facteurs qui exigent un réseau routier uniforme et cohérent.  
Je signale qu'il s'agit ici de la dernière motion d'initiative parlementaire du siècle. 
J'ai saisi la Chambre de cette motion il y a deux ans et demi, et je me suis toujours demandé pourquoi son étude a été si longtemps retardée. 
Désormais, je le sais. 
On a gardé la meilleure pour la fin. 
Quelle meilleure façon de terminer le siècle que de parler d'un nouveau réseau routier pour entrer dans le nouveau millénaire?  
Ma motion est fort simple. 
À l'heure actuelle, le financement des routes d'un pays qui en a désespérément besoin se fait au petit bonheur. 
Selon les estimations du ministère des Transports et le Comité parlementaire des transports, il faudra 17 milliards de dollars pour restaurer notre réseau routier. 
Il ne s'agit pas de l'améliorer mais bien de le restaurer, et ce, pour la bonne raison qu'on n'a pas consacré d'argent au réseau routier ces sept ou huit dernières années.  
On s'entend pour dire que de 38 à 40 p. 100 de notre réseau routier national est en état de dégradation, ce qui n'est ni conforme à la norme ni acceptable. 
Quelque 790 ponts de notre réseau routier national ont besoin de gros travaux de renforcement et de réparation. 
Il n'existe aucun programme de financement pour de tels travaux. 
Voilà dans quelle situation nous nous trouvons.  
La vieille politique qui a eu cours jusqu'en 1993-1994 consistait en un programme qui permettait au gouvernement fédéral de conclure des accords ponctuels avec les provinces. 
Ces accords étaient négociés à la pièce et, en ce qui concerne le partage des coûts, la formule du 50-50 était appliquée dans certaines provinces et pas dans d'autres. 
C'était donc une politique très incohérente, sans planification à long terme. 
Elle ne permettait pas aux provinces de planifier pour les collectivités, de prévoir les courants de trafic ou de tirer parti du libre-échange et de tous les autres programmes que nous avons mis en oeuvre et qui sont si importants.  
Qu'y a-t-il de mal à établir un programme de financement des routes? 
Je voudrais m'attarder un instant sur le cas du Canada atlantique, parce que c'est un bon exemple de ce qui peut se produire en l'absence d'un programme de financement des routes.  
Ces deux provinces misent sur les véhicules en provenance des autres provinces pour remplir leurs coffres. 
